package net.b5gamer.b5wars.io.unit;

public class LoadUnitException extends Exception {

	private static final long serialVersionUID = -5449919283633603177L;

	public LoadUnitException() {
		super();
	}

	public LoadUnitException(String message) {
		super(message);
	}

	public LoadUnitException(Throwable cause) {
		super(cause);
	}

	public LoadUnitException(String message, Throwable cause) {
		super(message, cause);
	}

}
